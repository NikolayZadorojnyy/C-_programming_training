// Выводим так чтобы было видно картинку
void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) Console.Write($" "); // что не закрашено выводим - пробел
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}
// Красим картинку по алгоритму — сначала идём вверх, потом влево, потом 
// вниз и вправо и красим если не закрашено
void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}
